namespace Hybrid.Graphics.Native;

/// <summary>
/// Defines common vertex element semantic names.
/// </summary>
public static class VertexSemantics
{
    /// <summary>
    /// The position vertex semantic name.
    /// </summary>
    public const string POSITION = "POSITION";

    /// <summary>
    /// The normal vertex semantic name.
    /// </summary>
    public const string NORMAL = "NORMAL";

    /// <summary>
    /// The tangent vertex semantic name.
    /// </summary>
    public const string TANGENT = "TANGENT";

    /// <summary>
    /// The bitangent vertex semantic name.
    /// </summary>
    public const string BITANGENT = "BITANGENT";

    /// <summary>
    /// The texture coordinate vertex semantic name.
    /// </summary>
    public const string TEXCOORD = "TEXCOORD";

    /// <summary>
    /// The color vertex semantic name.
    /// </summary>
    public const string COLOR = "COLOR";
}
